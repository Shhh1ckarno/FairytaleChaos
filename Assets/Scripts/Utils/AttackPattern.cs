// AttackPattern.cs (Полная и корректная версия)

public enum AttackPattern
{
    // --- АТАКИ ---

    // 1. Стандарт: Бьет только прямо напротив (Винни-Пух, Дровосек).
    StandardFront = 0,

    // 2. Гибкий Фронт (Буратино): Бьет прямо. Если линия пуста, бьет по диагонали назад.
    FlexibleFront = 1,

    // 3. Цель Сквозь Линию (Мальвина): Атакует через одну линию (с передней на заднюю и наоборот).
    TargetThroughLine = 2, // <-- ИСПРАВЛЕННОЕ НАЗВАНИЕ

    // --- ПОДДЕРЖКА ---

    // 4. Чистая Поддержка (Кальцифер): Не атакует, выполняет действия поддержки.
    SupportOnly = 99
}